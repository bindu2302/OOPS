
public class SampleArray {

	public static void main(String[] args) {
		
		int arr[] = new int[10];  // Creating an array of int type
		arr[0] = 10;
		arr[1] = 20;
		arr[2] = 30;
		arr[3] = 40;
		arr[4] = 50;          // Storing the data in array
		arr[5] = 60;
		arr[6] = 70;
		arr[7] = 80;
		arr[8] = 90;
		arr[9] = 100;
		
		System.out.print("Array elements are: ");
		for(int i=0;i<=9;i++) {
			System.out.print(arr[i] + " ");   // Retrieving the data of array

		}
		

	}

}
